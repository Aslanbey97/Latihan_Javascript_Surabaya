function hitungLuasPersegi(sisi) {
    var luas = sisi * sisi;
    return luas;
}

// Contoh penggunaan fungsi untuk menghitung luas persegi dengan sisi 5
var sisiPersegi = 5;
var luasPersegi = hitungLuasPersegi(sisiPersegi);
console.log("Luas persegi dengan sisi", sisiPersegi, "adalah", luasPersegi);
